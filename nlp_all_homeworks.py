def _with_auto_key(prefix: str, name: str, func: Callable[..., Any], counter: dict[str, int]) -> Callable[..., Any]:
    def wrapped(*args: Any, **kwargs: Any) -> Any:
        if kwargs.get("key") is None:
            counter["value"] += 1
            kwargs["key"] = f"{prefix}__auto__{counter['value']}__{name}"
        return func(*args, **kwargs)

    return wrapped


@contextmanager
def _patched_streamlit(prefix: str, tab_container: Any):
    """Make separately written Streamlit apps coexist inside one process."""
    originals: dict[str, Any] = {}
    counter = {"value": 0}

    def remember(name: str) -> Any:
        originals[name] = getattr(st, name)
        return originals[name]

    remember("set_page_config")
    remember("stop")
    remember("sidebar")
    remember("title")

    st.set_page_config = lambda *args, **kwargs: None
    st.stop = lambda: (_ for _ in ()).throw(_SubAppStopped())
    st.sidebar = tab_container

    def compact_title(body: Any, *args: Any, **kwargs: Any) -> Any:
        return st.markdown(f"### {body}")

    st.title = compact_title

    for widget_name in _PATCHABLE_WIDGETS:
        if hasattr(st, widget_name):
            original = remember(widget_name)
            setattr(st, widget_name, _with_auto_key(prefix, widget_name, original, counter))

    try:
        yield
    finally:
        for name, value in originals.items():
            setattr(st, name, value)


