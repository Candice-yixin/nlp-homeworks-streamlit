       'def _detect_zh_entity_type(mention: str) -> str:\n'
       '    person_markers = [\n'
       '        "先生", "女士", "同学", "学生", "老师", "少年", "男孩", "女孩", "女人", "男人", "母亲", "父亲",\n'
       '        "医生", "记者", "教授", "经理", "主席", "总统", "小伙", "孩子", "队员", "朋友",\n'
       '    ]\n'
       '    nonhuman_markers = [\n'
       '        "公司", "学校", "大学", "团队", "组织", "部门", "政府", "城市", "国家", "银行", "系统", "项目",\n'
       '        "产品", "手机", "汽车", "河", "书", "电影", "猪", "狗", "猫", "机器", "模型", "平台",\n'
       '    ]\n'
       '\n'
       '    if any(m in mention for m in person_markers):\n'
       '        return "person"\n'
       '    if any(m in mention for m in nonhuman_markers):\n'
       '        return "nonhuman"\n'
       '    return "unknown"\n'
