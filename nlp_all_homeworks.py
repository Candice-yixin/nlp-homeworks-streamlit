       '    ax.set_ylabel(choose_plot_label("维度2", "Dim 2"), fontsize=9)\n'
       '    ax.axhline(0, color="#d0d0d0", linewidth=0.7)\n'
       '    ax.axvline(0, color="#d0d0d0", linewidth=0.7)\n'
       '    ax.tick_params(labelsize=8)\n'
       '\n'
       '    if len(terms) <= 25:\n'
