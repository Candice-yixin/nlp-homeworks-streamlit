       'def resolve_chinese_coref(text: str) -> Tuple[List[List[str]], List[Dict[str, Any]]]:\n'
       '    pronoun_specs = [\n'
       '        ("他们的", "person"),\n'
       '        ("她们的", "person"),\n'
       '        ("它们的", "nonhuman"),\n'
       '        ("他们", "person"),\n'
       '        ("她们", "person"),\n'
       '        ("它们", "nonhuman"),\n'
       '        ("他的", "person"),\n'
       '        ("她的", "person"),\n'
       '        ("它的", "nonhuman"),\n'
       '        ("他", "person"),\n'
       '        ("她", "person"),\n'
       '        ("它", "nonhuman"),\n'
       '        ("自己", "any"),\n'
       '    ]\n'
       '\n'
       '    candidates = _extract_zh_candidates(text)\n'
       '    if not candidates:\n'
       '        return [], []\n'
       '\n'
       '    pron_pattern = re.compile("|".join(re.escape(p) for p, _ in pronoun_specs))\n'
       '    pron_type_map = {p: t for p, t in pronoun_specs}\n'
       '\n'
       '    cluster_by_anchor: Dict[Tuple[str, int], int] = {}\n'
       '    clusters: List[List[str]] = []\n'
       '    spans: List[Dict[str, Any]] = []\n'
       '\n'
       '    def add_mention(ci: int, mention: str, start: int, end: int) -> None:\n'
       '        if mention not in clusters[ci]:\n'
       '            clusters[ci].append(mention)\n'
       '        spans.append({"start": start, "end": end, "cluster": ci, "mention": mention})\n'
       '\n'
       '    for pm in pron_pattern.finditer(text):\n'
       '        pron = pm.group(0)\n'
       '        p_type = pron_type_map.get(pron, "any")\n'
       '        ant = _select_zh_antecedent(p_type, pm.start(), candidates)\n'
       '        if ant is None:\n'
       '            continue\n'
       '\n'
       '        key = (ant["text"], ant["start"])\n'
       '        if key not in cluster_by_anchor:\n'
       '            cluster_by_anchor[key] = len(clusters)\n'
       '            clusters.append([])\n'
       '            ci = cluster_by_anchor[key]\n'
       '            add_mention(ci, ant["text"], ant["start"], ant["end"])\n'
       '        else:\n'
       '            ci = cluster_by_anchor[key]\n'
       '\n'
       '        add_mention(ci, pron, pm.start(), pm.end())\n'
       '\n'
       '    # Merge exact repeated antecedent strings into the same cluster for cleaner display.\n'
       '    for (anchor_text, _), ci in cluster_by_anchor.items():\n'
       '        for cand in candidates:\n'
       '            if cand["text"] == anchor_text:\n'
       '                add_mention(ci, cand["text"], cand["start"], cand["end"])\n'
       '\n'
       '    spans = sorted(spans, key=lambda x: (x["start"], x["end"]))\n'
       '\n'
       '    # remove duplicate spans\n'
       '    uniq = []\n'
       '    seen = set()\n'
       '    for sp in spans:\n'
       '        k = (sp["start"], sp["end"], sp["cluster"])\n'
       '        if k in seen:\n'
       '            continue\n'
       '        seen.add(k)\n'
       '        uniq.append(sp)\n'
       '\n'
       '    clusters = [c for c in clusters if c]\n'
       '    return clusters, uniq\n'
       '\n'
       '\n'
       'def _find_non_overlapping_span(text: str, mention: str, used: List[Tuple[int, int]]) -> Tuple[int, int]:\n'
       '    mention = mention.strip()\n'
       '    if not mention:\n'
       '        return -1, -1\n'
       '\n'
       '    escaped = re.escape(mention)\n'
       '    if mention[0].isalnum() and mention[-1].isalnum():\n'
       '        pat = rf"(?<!\\w){escaped}(?!\\w)"\n'
       '    else:\n'
       '        pat = escaped\n'
       '\n'
       '    def _overlaps(s: int, e: int) -> bool:\n'
       '        for us, ue in used:\n'
       '            if not (e <= us or s >= ue):\n'
       '                return True\n'
       '        return False\n'
       '\n'
       '    for flags in (0, re.IGNORECASE):\n'
       '        for m in re.finditer(pat, text, flags=flags):\n'
       '            s, e = m.start(), m.end()\n'
       '            if not _overlaps(s, e):\n'
       '                return s, e\n'
       '    return -1, -1\n'
       '\n'
       '\n'
       'def build_coref_spans(text: str, clusters: List[List[str]]) -> List[Dict[str, Any]]:\n'
       '    spans: List[Dict[str, Any]] = []\n'
       '    used: List[Tuple[int, int]] = []\n'
       '\n'
       '    for ci, mentions in enumerate(clusters):\n'
       '        for mention in mentions:\n'
       '            s, e = _find_non_overlapping_span(text, mention, used)\n'
       '            if s >= 0:\n'
       '                used.append((s, e))\n'
       '                spans.append({"start": s, "end": e, "cluster": ci, "mention": mention})\n'
       '\n'
       '    spans.sort(key=lambda x: x["start"])\n'
       '    return spans\n'
       '\n'
