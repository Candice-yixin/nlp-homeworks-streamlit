def _run_homework(key: str, source: str, tab_container: Any) -> None:
    globals_dict = {
        "__name__": "__main__",
        "__file__": f"<embedded_{key}>",
        "__package__": None,
    }
    if key == "A2":
        source = _patch_a2_source(source)
    with _patched_streamlit(key.lower(), tab_container):
        try:
            exec(compile(source, f"<embedded_{key}>", "exec"), globals_dict)
            _inject_readability_overrides()
        except _SubAppStopped:
            _inject_readability_overrides()
            return
        except Exception:
            st.error(f"{key} 运行失败")
            st.code(traceback.format_exc(), language="python")
            _inject_readability_overrides()


