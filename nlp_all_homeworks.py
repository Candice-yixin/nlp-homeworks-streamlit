       '\n'
       'def _extract_zh_candidates(text: str) -> List[Dict[str, Any]]:\n'
       '    pronouns = {\n'
       '        "他", "她", "它", "他们", "她们", "它们", "他的", "她的", "它的", "他们的", "她们的", "它们的", "自己",\n'
       '    }\n'
       '    stop_words = {\n'
       '        "这个", "那个", "这些", "那些", "一个", "一种", "一些", "因为", "所以", "但是", "如果", "然后", "以及", "而且",\n'
       '        "并且", "为了", "其中", "其间", "可以", "应该", "已经", "进行", "表示", "认为", "我们", "你们", "他们", "她们",\n'
       '        "它们", "没有", "不是", "还有", "就是", "那里", "这里", "这样", "那样", "此次", "本次",\n'
       '    }\n'
       '\n'
       '    candidates: List[Dict[str, Any]] = []\n'
       '\n'
       '    # Typed mentions first: improves pronoun antecedent quality.\n'
       '    person_markers = [\n'
       '        "先生", "女士", "同学", "学生", "老师", "少年", "男孩", "女孩", "女人", "男人", "母亲", "父亲",\n'
       '        "医生", "记者", "教授", "经理", "主席", "总统", "小伙", "孩子", "队员", "朋友",\n'
       '    ]\n'
       '    nonhuman_markers = [\n'
       '        "公司", "学校", "大学", "团队", "组织", "部门", "政府", "城市", "国家", "银行", "系统", "项目",\n'
       '        "产品", "手机", "汽车", "河", "书", "电影", "猪", "狗", "猫", "机器", "模型", "平台", "猹",\n'
       '    ]\n'
       '\n'
       '    for marker in person_markers:\n'
       '        for m in re.finditer(re.escape(marker), text):\n'
       '            candidates.append({"text": marker, "start": m.start(), "end": m.end(), "type": "person"})\n'
       '\n'
       '    for marker in nonhuman_markers:\n'
       '        for m in re.finditer(re.escape(marker), text):\n'
       '            candidates.append({"text": marker, "start": m.start(), "end": m.end(), "type": "nonhuman"})\n'
       '\n'
       '    # Fallback unknown mentions for cases without explicit markers.\n'
       '    for m in re.finditer(r"[一-鿿]{2,8}", text):\n'
       '        mention = m.group(0)\n'
       '        if mention in pronouns or mention in stop_words:\n'
       '            continue\n'
       '        if mention.startswith("第") and mention[1:].isdigit():\n'
       '            continue\n'
       '        if any(ch in mention for ch in "将把被从向在了着过是有"):\n'
       '            continue\n'
       '        candidates.append(\n'
       '            {\n'
       '                "text": mention,\n'
       '                "start": m.start(),\n'
       '                "end": m.end(),\n'
       '                "type": _detect_zh_entity_type(mention),\n'
       '            }\n'
       '        )\n'
       '\n'
       '    # Deduplicate candidates by exact span.\n'
       '    dedup = []\n'
       '    seen = set()\n'
       '    for c in sorted(candidates, key=lambda x: (x["start"], x["end"])):\n'
       '        k = (c["start"], c["end"])\n'
       '        if k in seen:\n'
       '            continue\n'
       '        seen.add(k)\n'
       '        dedup.append(c)\n'
       '    return dedup\n'
       '\n'
       '\n'
       'def _select_zh_antecedent(\n'
       '    pron_type: str,\n'
       '    pron_start: int,\n'
       '    candidates: List[Dict[str, Any]],\n'
       ') -> Optional[Dict[str, Any]]:\n'
       '    prev = [c for c in candidates if c["end"] <= pron_start]\n'
       '    if not prev:\n'
       '        return None\n'
       '\n'
       '    def nearest(items: List[Dict[str, Any]]) -> Optional[Dict[str, Any]]:\n'
       '        if not items:\n'
       '            return None\n'
       '        return min(items, key=lambda c: (pron_start - c["end"], c["end"] - c["start"]))\n'
       '\n'
       '    if pron_type == "person":\n'
       '        typed = [c for c in prev if c["type"] == "person"]\n'
       '        return nearest(typed) or nearest([c for c in prev if c["type"] == "unknown"])\n'
       '\n'
       '    if pron_type == "nonhuman":\n'
       '        typed = [c for c in prev if c["type"] == "nonhuman"]\n'
       '        return nearest(typed) or nearest([c for c in prev if c["type"] == "unknown"])\n'
       '\n'
       '    return nearest(prev)\n'
       '\n'
