       'def _is_discourse_level_and_or(canonical: str, sentence: str, start: int, end: int, doc=None) -> bool:\n'
       '    """Filter out phrase-level and/or (e.g., proper noun lists) to reduce false positives."""\n'
       '    if canonical not in {"and", "or"}:\n'
       '        return True\n'
       '\n'
       '    tok = _find_token_at_char(doc, start, end)\n'
       '    if tok is not None:\n'
       '        if tok.dep_ == "cc" and tok.head.pos_ in {"NOUN", "PROPN", "ADJ", "NUM"}:\n'
       '            return False\n'
       '        if tok.dep_ == "cc" and tok.head.pos_ in {"VERB", "AUX"}:\n'
       '            return True\n'
       '\n'
       '    # Fallback without spaCy parse: suppress nominal list patterns like\n'
       '    # "Finance and Economics" / "research and development".\n'
       '    left_word_m = re.search(r"([A-Za-z][\\w\'-]*)\\W*$", sentence[:start])\n'
       '    right_word_m = re.search(r"^\\W*([A-Za-z][\\w\'-]*)", sentence[end:])\n'
       '    if left_word_m and right_word_m:\n'
       '        left_word = left_word_m.group(1)\n'
       '        right_word = right_word_m.group(1)\n'
       '\n'
       '        # Proper-noun pair near conjunction is usually phrase-level, not discourse-level.\n'
       '        if left_word[:1].isupper() and right_word[:1].isupper():\n'
       '            return False\n'
       '\n'
       '        verbish = re.compile(r"(ed|ing)$", flags=re.IGNORECASE)\n'
       '        if not verbish.search(left_word) and not verbish.search(right_word):\n'
       '            left = sentence[max(0, start - 45) : start].lower()\n'
       '            right = sentence[end : min(len(sentence), end + 45)].lower()\n'
       '            clause_verb_pat = '
       'r"\\b(is|are|was|were|be|been|being|has|have|had|do|does|did|am|\\w+ed|\\w+ing)\\b"\n'
       '            if not (re.search(clause_verb_pat, left) and re.search(clause_verb_pat, right)):\n'
       '                return False\n'
       '\n'
       '    left = sentence[max(0, start - 45) : start].lower()\n'
       '    right = sentence[end : min(len(sentence), end + 45)].lower()\n'
       '    verb_pat = r"\\b(is|are|was|were|be|been|being|has|have|had|do|does|did|am|\\w+ed|\\w+ing)\\b"\n'
       '    return bool(re.search(verb_pat, left) and re.search(verb_pat, right))\n'
       '\n'
       '\n'
       'def _resolve_ambiguous_category(canonical: str, sentence: str, start: int, end: int, doc=None) -> str:\n'
       '    """Context-aware category disambiguation for ambiguous connectives like \'since\'."""\n'
       '    base = CONNECTIVE_CATEGORY_MAP.get(canonical, "UNKNOWN")\n'
       '    if canonical != "since":\n'
       '        return base\n'
       '\n'
       '    left = sentence[:start].lower()\n'
       '    right = sentence[end:].lower()\n'
       '\n'
       '    # Rule 1: perfect aspect before since -> usually temporal (e.g., has been living ... since ...)\n'
       '    perfect_pat = r"\\b(has|have|had)\\b(?:\\s+\\w+){0,3}\\s+\\w+(?:ed|en|ing)\\b"\n'
       '    if "ever since" in left or re.search(perfect_pat, left):\n'
       '        return "TEMPORAL"\n'
       '\n'
       '    # Rule 2: explicit temporal clue words near right context\n'
       '    temporal_clues = {\n'
       '        "year",\n'
       '        "years",\n'
       '        "month",\n'
       '        "months",\n'
       '        "week",\n'
       '        "weeks",\n'
       '        "day",\n'
       '        "days",\n'
       '        "ago",\n'
       '        "then",\n'
       '        "graduated",\n'
       '        "born",\n'
       '        "yesterday",\n'
       '        "today",\n'
       '        "last",\n'
       '        "next",\n'
       '    }\n'
       '    if any(clue in right for clue in temporal_clues):\n'
       '        return "TEMPORAL"\n'
       '\n'
       '    # Rule 3: sentence-initial "Since ...," often causal (PDTB Contingency)\n'
       '    if not left.strip():\n'
       '        comma_pos = right.find(",")\n'
       '        if 0 <= comma_pos <= 40:\n'
       '            return "CONTINGENCY"\n'
       '\n'
       '    tok = _find_token_at_char(doc, start, end)\n'
       '    if tok is not None:\n'
       '        # If its clause head is linked to a perfect/temporal construction, prefer TEMPORAL\n'
       '        head = tok.head\n'
       '        head_and_anc = [head] + list(head.ancestors)\n'
       '        for t in head_and_anc:\n'
       '            for c in t.children:\n'
       '                if c.dep_ == "aux" and c.lemma_.lower() == "have":\n'
       '                    return "TEMPORAL"\n'
       '\n'
       '    return base\n'
       '\n'
       '\n'
       'def detect_connectives(sentence: str, doc=None):\n'
       '    """Detect explicit discourse connectives and assign PDTB top-level category."""\n'
       '    keys = sorted(CONNECTIVE_CATEGORY_MAP.keys(), key=len, reverse=True)\n'
       '    pattern_parts = []\n'
       '    for k in keys:\n'
       '        part = re.escape(k).replace(r"\\ ", r"\\s+")\n'
       '        pattern_parts.append(part)\n'
       '    pattern = re.compile(r"\\b(?:" + "|".join(pattern_parts) + r")\\b", flags=re.IGNORECASE)\n'
       '\n'
       '    found = []\n'
       '    for m in pattern.finditer(sentence):\n'
       '        raw = m.group(0)\n'
       '        canonical = re.sub(r"\\s+", " ", raw.strip().lower())\n'
       '\n'
       '        if not _is_discourse_level_and_or(canonical, sentence, m.start(), m.end(), doc=doc):\n'
       '            continue\n'
       '\n'
       '        category = _resolve_ambiguous_category(canonical, sentence, m.start(), m.end(), doc=doc)\n'
       '\n'
       '        found.append(\n'
       '            {\n'
       '                "text": raw,\n'
       '                "canonical": canonical,\n'
       '                "category": category,\n'
       '                "start": m.start(),\n'
       '                "end": m.end(),\n'
       '            }\n'
       '        )\n'
       '    return found\n'
       '\n'
       '\n'
       'def highlight_connectives(sentence: str, matches) -> str:\n'
       '    """Render sentence with colored connective tags like: although [COMPARISON]."""\n'
       '    if not matches:\n'
       '        return html.escape(sentence)\n'
       '\n'
       '    chunks = []\n'
       '    cursor = 0\n'
       '    for m in matches:\n'
       '        s, e = m["start"], m["end"]\n'
       '        chunks.append(html.escape(sentence[cursor:s]))\n'
       '\n'
       '        cat = m["category"]\n'
       '        word = html.escape(sentence[s:e])\n'
       '        chunks.append(\n'
       '            f"<span class=\'conn-word\'>{word}</span> "\n'
       '            f"<span class=\'conn-cat cat-{cat}\'><b>[{cat}]</b></span>"\n'
       '        )\n'
       '        cursor = e\n'
       '\n'
       '    chunks.append(html.escape(sentence[cursor:]))\n'
       '    return "".join(chunks)\n'
       '\n'
       '\n'
       'def split_args_by_connective(sentence: str, match: dict) -> tuple[str, str]:\n'
       '    """\n'
       '    Simplified Arg extraction:\n'
       '    - Default: Arg1 = left context, Arg2 = right context\n'
       '    - If connective at sentence start, fallback to split at first comma after connective.\n'
       '    """\n'
       '    s, e = match["start"], match["end"]\n'
       '\n'
       '    if s > 0:\n'
       '        arg1 = sentence[:s].strip(" ,;:-")\n'
       '        arg2 = sentence[e:].strip(" ,;:-")\n'
       '        return arg1 or "-", arg2 or "-"\n'
       '\n'
       '    after = sentence[e:].lstrip()\n'
       '    comma_idx = after.find(",")\n'
       '    if comma_idx != -1:\n'
       '        # include leading connective clause in Arg1 for better readability\n'
       '        arg1 = (sentence[:e] + " " + after[:comma_idx]).strip(" ,;:-")\n'
       '        arg2 = after[comma_idx + 1 :].strip(" ,;:-")\n'
       '        return arg1 or "-", arg2 or "-"\n'
       '\n'
       '    arg1 = sentence[:e].strip(" ,;:-")\n'
       '    arg2 = sentence[e:].strip(" ,;:-")\n'
       '    return arg1 or "-", arg2 or "-"\n'
       '\n'
