       '    return model\n'
       '\n'
       '\n'
       '@st.cache_resource(show_spinner=False)\n'
       'def train_fasttext_cached(\n'
       '    corpus_tuple: tuple[tuple[str, ...], ...],\n'
       '    vector_size: int,\n'
       '    window: int,\n'
       '    min_count: int,\n'
       '    epochs: int,\n'
       '):\n'
       '    if FastText is None:\n'
       '        raise ImportError("FastText 不可用")\n'
       '\n'
       '    sentences = [list(s) for s in corpus_tuple]\n'
       '    model = FastText(\n'
       '        sentences=sentences,\n'
       '        vector_size=vector_size,\n'
       '        window=window,\n'
       '        min_count=min_count,\n'
       '        workers=1,\n'
       '        epochs=epochs,\n'
       '        seed=42,\n'
       '    )\n'
       '    return model\n'
       '\n'
       '\n'
       'def build_tfidf(docs: list[str]):\n'
       '    vectorizer = TfidfVectorizer(stop_words="english")\n'
       '    matrix = vectorizer.fit_transform(docs)\n'
       '    terms = vectorizer.get_feature_names_out()\n'
       '    return matrix, terms\n'
       '\n'
       '\n'
       'def build_onehot(docs: list[str]):\n'
       '    vectorizer = CountVectorizer(stop_words="english", binary=True)\n'
       '    matrix = vectorizer.fit_transform(docs)\n'
       '    terms = vectorizer.get_feature_names_out()\n'
       '    return matrix, terms\n'
       '\n'
       '\n'
       'def top_keywords_from_tfidf(tfidf_matrix, terms: np.ndarray, top_k: int = 5):\n'
       '    weights = np.asarray(tfidf_matrix.sum(axis=0)).ravel()\n'
       '    if weights.size == 0:\n'
       '        return []\n'
       '    top_indices = np.argsort(weights)[::-1][:top_k]\n'
       '    return [(terms[i], float(weights[i])) for i in top_indices]\n'
       '\n'
       '\n'
       'def lsa_2d_projection(matrix):\n'
       '    """\n'
       '    Reduce vocabulary vectors to 2D.\n'
       '    matrix shape: (n_docs, n_terms)\n'
       '    We transpose to (n_terms, n_docs) so each point is a term.\n'
       '    """\n'
       '    term_doc_matrix = matrix.T\n'
       '    if term_doc_matrix.shape[0] < 2 or term_doc_matrix.shape[1] < 2:\n'
       '        return None\n'
       '\n'
       '    svd = TruncatedSVD(n_components=2, random_state=42)\n'
       '    coords = svd.fit_transform(term_doc_matrix)\n'
       '    return coords\n'
       '\n'
       '\n'
