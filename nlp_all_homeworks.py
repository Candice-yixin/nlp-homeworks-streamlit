       'def rule_based_edu_segment(text: str, nlp) -> List[str]:\n'
       '    """\n'
       '    Heuristic baseline EDU segmentation using punctuation + conjunction/dependency cues.\n'
       '    """\n'
       '    doc = nlp(text)\n'
       '    tokens = [tok for tok in doc if not tok.is_space]\n'
       '    if not tokens:\n'
       '        return []\n'
       '\n'
       '    boundaries = set()\n'
       '    dep_triggers = {"mark", "advcl", "relcl", "ccomp", "xcomp", "conj"}\n'
       '\n'
       '    for i, tok in enumerate(tokens):\n'
       '        nxt = tokens[i + 1] if i + 1 < len(tokens) else None\n'
       '\n'
       '        if tok.text in {".", "?", "!", ";"}:\n'
       '            boundaries.add(i)\n'
       '            continue\n'
       '\n'
       '        if nxt is None:\n'
       '            boundaries.add(i)\n'
       '            continue\n'
       '\n'
       '        if tok.text == "," and (nxt.pos_ in {"SCONJ", "CCONJ"} or nxt.dep_ in dep_triggers):\n'
       '            boundaries.add(i)\n'
       '            continue\n'
       '\n'
       '        if nxt.pos_ == "SCONJ" or nxt.dep_ in dep_triggers:\n'
       '            boundaries.add(i)\n'
       '            continue\n'
       '\n'
       '    boundaries.add(len(tokens) - 1)\n'
       '\n'
       '    edus = []\n'
       '    start_char = tokens[0].idx\n'
       '    for i, tok in enumerate(tokens):\n'
       '        if i in boundaries:\n'
       '            end_char = tok.idx + len(tok.text)\n'
       '            seg = text[start_char:end_char].strip()\n'
       '            if seg:\n'
       '                edus.append(seg)\n'
       '            if i + 1 < len(tokens):\n'
       '                start_char = tokens[i + 1].idx\n'
       '\n'
       '    return edus\n'
