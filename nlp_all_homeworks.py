       '    else:\n'
       '        # 只标注离原点较远的词，避免过度重叠\n'
       '        norms = np.linalg.norm(coords, axis=1)\n'
       '        annotate_idx = np.argsort(norms)[::-1][:25]\n'
       '\n'
       '    for i in annotate_idx:\n'
       '        ax.text(coords[i, 0], coords[i, 1], terms[i], fontsize=6)\n'
       '\n'
