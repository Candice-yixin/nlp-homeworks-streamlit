       'def _find_token_at_char(doc, start: int, end: int):\n'
       '    if doc is None:\n'
       '        return None\n'
       '    span = doc.char_span(start, end, alignment_mode="expand")\n'
       '    if span is not None and len(span) > 0:\n'
       '        return span[0]\n'
       '    for tok in doc:\n'
       '        tok_s = tok.idx\n'
       '        tok_e = tok.idx + len(tok.text)\n'
       '        if tok_s <= start < tok_e:\n'
       '            return tok\n'
       '    return None\n'
       '\n'
